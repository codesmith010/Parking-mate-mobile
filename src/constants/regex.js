export const REGEX = {
  email:
    /^(([^<>()[\]\\.,;:\s@\"]+(\.[^<>()[\]\\.,;:\s@\"]+)*)|(\".+\"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
  firstName: /^(?=[A-Za-z\s]{1,50}$)[A-Z][a-z]*(?:\s[A-Z][a-z]*){0,2}$/,
  lastName: /^(?=[A-Za-z\s]{1,50}$)[A-Z][a-z]*(?:\s[A-Z][a-z]*){0,2}$/,
  password: /^(?=.*[0-9])(?=.*[!-_@"£$%^&*])[A-Za-z0-9!-_@"£$%^&*]{8,32}$/,
  phoneNumber: /^[0-9]{10}$/,
  digit: /[^0-9]/g,
  decimal: /^\d*\.?\d+$/,
  letter: /[^a-zA-Z ]/,
  spaceLetter: /[^a-zA-Z]/,
};
